package day01;

public class Actor {
    private String name;
    private int ageOfBirth;

    public Actor(String name, int ageOfBirth) {
        this.name = name;
        this.ageOfBirth = ageOfBirth;
    }

    public String getName() {
        return name;
    }

    public int getAgeOfBirth() {
        return ageOfBirth;
    }

}
